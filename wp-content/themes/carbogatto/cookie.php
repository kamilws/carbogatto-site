        <div class="par">Performance cookies</div>
        <div class="par">Performance cookies collect information on how a website is used – for example which pages a
          visitor opens most frequently and whether they receive error messages from a page. These cookies do not save
          any information that permits identification of the user. The collected information will be aggregated and thus
          will be analysed in a non-attributable way to a specific person. These cookies are used exclusively to improve
          the performance of a website and thus the user experience.
        </div>
        <div class="par">Functional cookies</div>
        <div class="par">Functional cookies enable a website to save details that have already been provided (e.g. user
          name, language choices or your location) and offer the user improved, more personal functions. Functional
          cookies are used, for example, to enable requested functions such as the playback of videos. These cookies
          collect anonymised information; they cannot track your movement on other websites.
        </div>
